package com.model.vo;

import aming.framework.db.ColumnProperty;
import java.util.Date;

@ColumnProperty("t_task")
public class TaskPojo {
	@ColumnProperty("task_id")
	private Integer taskId;

	@ColumnProperty("merchant_id")
	private String merchantId;

	@ColumnProperty("order_no")
	private String orderNo;

	@ColumnProperty("request_body")
	private String requestBody;

	@ColumnProperty("task_type")
	private String taskType;

	@ColumnProperty("channel_code")
	private String channelCode;

	@ColumnProperty("task_status")
	private String taskStatus;

	@ColumnProperty("task_detail")
	private String taskDetail;

	@ColumnProperty("card_no")
	private String cardNo;

	@ColumnProperty("amount")
	private Integer amount;

	@ColumnProperty("id_no")
	private String idNo;

	@ColumnProperty("create_time")
	private Date createTime;

	@ColumnProperty("update_time")
	private Date updateTime;

	@ColumnProperty("next_check_time")
	private Date nextCheckTime;

	@ColumnProperty("check_counter")
	private Integer checkCounter;



	public Integer getTaskId() {
		 return taskId;
	}

	public void setTaskId(Integer taskId) {
		 this.taskId=taskId;
	}

	public String getMerchantId() {
		 return merchantId;
	}

	public void setMerchantId(String merchantId) {
		 this.merchantId=merchantId;
	}

	public String getOrderNo() {
		 return orderNo;
	}

	public void setOrderNo(String orderNo) {
		 this.orderNo=orderNo;
	}

	public String getRequestBody() {
		 return requestBody;
	}

	public void setRequestBody(String requestBody) {
		 this.requestBody=requestBody;
	}

	public String getTaskType() {
		 return taskType;
	}

	public void setTaskType(String taskType) {
		 this.taskType=taskType;
	}

	public String getChannelCode() {
		 return channelCode;
	}

	public void setChannelCode(String channelCode) {
		 this.channelCode=channelCode;
	}

	public String getTaskStatus() {
		 return taskStatus;
	}

	public void setTaskStatus(String taskStatus) {
		 this.taskStatus=taskStatus;
	}

	public String getTaskDetail() {
		 return taskDetail;
	}

	public void setTaskDetail(String taskDetail) {
		 this.taskDetail=taskDetail;
	}

	public String getCardNo() {
		 return cardNo;
	}

	public void setCardNo(String cardNo) {
		 this.cardNo=cardNo;
	}

	public Integer getAmount() {
		 return amount;
	}

	public void setAmount(Integer amount) {
		 this.amount=amount;
	}

	public String getIdNo() {
		 return idNo;
	}

	public void setIdNo(String idNo) {
		 this.idNo=idNo;
	}

	public Date getCreateTime() {
		 return createTime;
	}

	public void setCreateTime(Date createTime) {
		 this.createTime=createTime;
	}

	public Date getUpdateTime() {
		 return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		 this.updateTime=updateTime;
	}

	public Date getNextCheckTime() {
		 return nextCheckTime;
	}

	public void setNextCheckTime(Date nextCheckTime) {
		 this.nextCheckTime=nextCheckTime;
	}

	public Integer getCheckCounter() {
		 return checkCounter;
	}

	public void setCheckCounter(Integer checkCounter) {
		 this.checkCounter=checkCounter;
	}

}